package com.class3;

public class IfElseIf {

	public static void main(String[] args) {
		int num1=100;
		int num2=99;
		if(num1>num2) {
			System.out.println("num1 is larger than num2");
		}else if(num1==num2) {
			System.out.println("num1 is equal to num2");
		}else {
			System.out.println("num1 is smaller than num2");
		}
	}

}
