package com.class8;

public class ForLoop {

	public static void main(String[] args) {
		
		for(int i=1;i<=100;i++) {
			System.out.print(i+" ");
		}
		System.out.println("##########################");
	
	for(int r=100;r>=1;r--) {
		System.out.println(r);
		
	}System.out.println("##########################");
	for(int v=20;v>=0;v-=2) {
		System.out.println(v);
		
	}System.out.println("##########################");
	for(int f=0;f<=20;f+=2) {
		System.out.println(f);
	}System.out.println("##########################");
	for(int h=21;h<=49;h+=2) {
		System.out.println(h);
	}System.out.println("##########################");
	for(int g=49;g>=21;g-=2) {
		System.out.println(g);
	}
	
}
}